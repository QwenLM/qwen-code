/**
 * Length of a JSON escape sequence starting with `\\`. Handles the six-char
 * `\\uXXXX` form — otherwise a value like `"sk-abc\\u0022,..."` would let the
 * embedded `\\u0022` close the string early and the rest of the buffer would
 * parse as additional keys, enabling settings.json key injection.
 */
function jsonEscapeLength(text: string, backslashIdx: number): number {
  return text[backslashIdx + 1] === 'u' ? 6 : 2;
}

/**
 * Strip single-line (`//`) and multi-line comments from JSONC content
 * so that `JSON.parse` can handle ~/.qwen/settings.json files that
 * contain comments (common when hand-edited or generated by CLI).
 */
function stripJsonComments(text: string): string {
  let result = '';
  let i = 0;
  while (i < text.length) {
    // String literal — copy verbatim (handles escaped quotes)
    if (text[i] === '"') {
      let j = i + 1;
      while (j < text.length) {
        if (text[j] === '\\') {
          j += jsonEscapeLength(text, j);
        } else if (text[j] === '"') {
          j++;
          break;
        } else {
          j++;
        }
      }
      result += text.slice(i, j);
      i = j;
    } else if (text[i] === '/' && text[i + 1] === '/') {
      // Single-line comment — skip until newline
      const nl = text.indexOf('\n', i);
      i = nl === -1 ? text.length : nl;
    } else if (text[i] === '/' && text[i + 1] === '*') {
      // Multi-line comment — skip until */
      const end = text.indexOf('*/', i + 2);
      i = end === -1 ? text.length : end + 2;
    } else {
      result += text[i];
      i++;
    }
  }
  return result;
}

/**
 * Strip trailing commas inside arrays/objects (`,]` / `,}`) — VSCode's own
 * settings.json allows them and they crash strict JSON.parse. Uses a
 * character-by-character scanner so that the `,]` substring inside a string
 * literal (e.g. `"MY_VAR": ",]"`) is preserved unchanged — a regex would
 * silently rewrite it and corrupt the value.
 */
function stripTrailingCommas(text: string): string {
  let result = '';
  let i = 0;
  while (i < text.length) {
    const ch = text[i];
    if (ch === '"') {
      // Copy the entire string literal (including escapes) verbatim. Must
      // use the same jsonEscapeLength helper as stripJsonComments so that
      // `"` is not mis-parsed as `\u` + early-terminating `"`.
      let j = i + 1;
      while (j < text.length) {
        if (text[j] === '\\') {
          j += jsonEscapeLength(text, j);
          continue;
        }
        if (text[j] === '"') {
          j++;
          break;
        }
        j++;
      }
      result += text.slice(i, j);
      i = j;
      continue;
    }
    if (ch === ',') {
      // Drop comma only if the next non-whitespace char is } or ].
      let j = i + 1;
      while (j < text.length && /\s/.test(text[j]!)) j++;
      if (j < text.length && (text[j] === ']' || text[j] === '}')) {
        i++;
        continue;
      }
    }
    result += ch;
    i++;
  }
  return result;
}

/**
 * Read ~/.qwen/settings.json. Returns {} if the file is missing.
 * Parse errors are logged and re-thrown so callers don't silently destroy a
 * malformed file by treating it as empty and then overwriting it.
 * Handles JSONC (JSON with comments + trailing commas) for hand-edited files.
 */
function readSettings(): Record<string, unknown> {
  const settingsPath = Storage.getGlobalSettingsPath();
  let content: string;
  try {
    content = fs.readFileSync(settingsPath, 'utf-8');
  } catch (err) {
    if ((err as NodeJS.ErrnoException).code === 'ENOENT') {
      return {};
    }
    throw err;
  }
  try {
    return JSON.parse(
      stripTrailingCommas(stripJsonComments(content)),
    ) as Record<string, unknown>;
  } catch (err) {
    // Surface an actionable message rather than the raw SyntaxError. The
    // caller's catch will see this; refusing to overwrite a malformed file
    // is the whole point — better than silently destroying user state by
    // treating it as `{}`.
    console.error(
      `[settingsWriter] Failed to parse ${settingsPath}; refusing to overwrite a malformed file.`,
      err,
    );
    const message =
      err instanceof Error ? `${err.name}: ${err.message}` : String(err);
    throw new Error(
      `Cannot parse ~/.qwen/settings.json (${message}). ` +
        `Standard JSONC (// comments, /* */, trailing commas) is supported, ` +
        `but non-standard JSON features (unquoted keys, single quotes) are not. ` +
        `Please fix or delete ${settingsPath} and try again.`,
      { cause: err instanceof Error ? err : undefined },
    );
  }
}

/**
 * Write ~/.qwen/settings.json atomically (temp file + rename), creating the
 * directory if needed. Atomic rename prevents leaving a half-written file
 * behind on EACCES / disk-full / process crash mid-write.
 */
function writeSettings(settings: Record<string, unknown>): void {
  const settingsPath = Storage.getGlobalSettingsPath();
  const dir = path.dirname(settingsPath);
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true, mode: SETTINGS_DIR_MODE });
  }
  const tmpPath = `${settingsPath}.${process.pid}.${Date.now()}.tmp`;
  // Create the temp file with owner-only permissions so secrets in `env.*`
  // are never observable to other local users even briefly.
  fs.writeFileSync(tmpPath, JSON.stringify(settings, null, 2), {
    encoding: 'utf-8',
    mode: SETTINGS_FILE_MODE,
  });
  try {
    fs.renameSync(tmpPath, settingsPath);
  } catch (renameErr) {
    // renameSync can fail on Windows when a watcher / antivirus holds the
    // target (EPERM/EBUSY). The temp file otherwise lingers in ~/.qwen
    // containing API keys — clean it up so secrets don't accumulate on
    // disk across repeated failed writes.
    try {
      fs.unlinkSync(tmpPath);
    } catch {
      /* best-effort cleanup; surface the original rename error below */
    }
    throw renameErr;
  }
  // Tighten any pre-existing settings file inherited from older writes that
  // used the default umask. chmodSync is a no-op on the just-written file but
  // covers the case where the file was created earlier with looser bits.
  try {
    fs.chmodSync(settingsPath, SETTINGS_FILE_MODE);
  } catch {
    // Best effort — surface nothing to the user if the FS rejects chmod
    // (e.g. Windows, mounted FS without POSIX permissions).
  }
}

/**
 * Ensure nested objects exist at the given key path.
 */
function ensureNestedObject(
  obj: Record<string, unknown>,
  ...keys: string[]
): Record<string, unknown> {
  let current = obj;
  for (const key of keys) {
    if (!current[key] || typeof current[key] !== 'object') {
      current[key] = {};
    }
    current = current[key] as Record<string, unknown>;
  }
  return current;
}

/**
 * Find OpenAI-compatible model entries from modelProviders.
 * CLI uses AuthType.USE_OPENAI ('openai') as the key, but some legacy
 * configs may use other keys. Check both.
 */
function findOpenaiModels(
  modelProviders: Record<string, unknown> | undefined,
): Array<Record<string, unknown>> {
  if (!modelProviders) {
    return [];
  }
  for (const key of [AuthType.USE_OPENAI, 'use_openai']) {
    const arr = modelProviders[key];
    if (Array.isArray(arr) && arr.length > 0) {
      return arr as Array<Record<string, unknown>>;
    }
  }
  return [];
}

function clearInactiveSubscriptionPlanState(
  settings: Record<string, unknown>,
  active: {
    envKey: string;
    legacyMetadataKey: string;
    providerMetadataKey: SubscriptionProviderMetadataKey;
  },
): void {
  const env = settings.env as Record<string, unknown> | undefined;
  if (env) {
    for (const plan of SUBSCRIPTION_PLAN_OPTIONS) {
      if (plan.envKey !== active.envKey) {
        delete env[plan.envKey];
      }
    }
    // Do not delete API_KEY_ENV_KEY. It belongs to the api-key auth path,
    // not a subscription plan. writeSubscriptionPlanConfig preserves
    // non-subscription (custom api-key) model entries, which still
    // reference this env var — deleting it breaks them silently. The loop
    // above already removes inactive subscription-plan env keys.
  }

  for (const plan of SUBSCRIPTION_PLAN_OPTIONS) {
    if (plan.metadataKey !== active.legacyMetadataKey) {
      delete settings[plan.metadataKey];
    }
  }

  const providerMetadata = settings.providerMetadata as
    | Record<string, unknown>
    | undefined;
  if (providerMetadata) {
    for (const key of SUBSCRIPTION_PROVIDER_METADATA_KEYS) {
      if (key !== active.providerMetadataKey) {
        delete providerMetadata[key];
      }
    }
  }
}

function writeSubscriptionPlanConfig(params: {
  apiKey: string;
  planConfig: SubscriptionPlanConfig;
  providerMetadataKey: SubscriptionProviderMetadataKey;
  metadata?: Record<string, unknown>;
}): void {
  const { apiKey, planConfig, providerMetadataKey, metadata = {} } = params;
  const settings = readSettings();

  const auth = ensureNestedObject(settings, 'security', 'auth');
  auth.selectedType = AuthType.USE_OPENAI;

  const env = ensureNestedObject(settings, 'env');
  env[planConfig.envKey] = apiKey;
  clearInactiveSubscriptionPlanState(settings, {
    envKey: planConfig.envKey,
    legacyMetadataKey: planConfig.metadataKey,
    providerMetadataKey,
  });

  const providers = ensureNestedObject(settings, 'modelProviders');
  const existing = findOpenaiModels(
    settings.modelProviders as Record<string, unknown>,
  );
  const nonSubscriptionPlan = existing.filter(
    (entry) =>
      !isSubscriptionPlanConfig(
        entry.baseUrl as string,
        entry.envKey as string,
      ),
  );
  const planModels = planConfig.template.map((model) => ({
    ...model,
    envKey: planConfig.envKey,
  }));
  providers[AuthType.USE_OPENAI] = [...planModels, ...nonSubscriptionPlan];

  const providerMetadata = ensureNestedObject(settings, 'providerMetadata');
  providerMetadata[providerMetadataKey] = {
    baseUrl: planConfig.baseUrl,
    version: planConfig.version,
    ...metadata,
  };
  delete settings[planConfig.metadataKey];

  const defaultModelId = planConfig.template[0]?.id ?? 'qwen3.5-plus';
  settings.model = { name: defaultModelId };

  writeSettings(settings);
}

